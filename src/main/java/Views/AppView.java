    /**
     * Ingresa una persona a la base de datos
     *
     * @return void
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        IPersonaController controllerPerson2 = this.intancePersonaControlle();
        Runnable ru = () -> {
            if (this.valiadarCampos()) {
                lblCargando.setVisible(true);
                controllerPerson2.insertOnePersonDb(new Persona(txtNombresCompletos.getText(),
                        txtApellidosCompletos.getText(),
                        txtTelefono.getText(),
                        txtDireccion.getText(),
                        LocalDateTime.now(),
                        LocalDateTime.now())
                );
                lblCargando.setVisible(false);
                this.whaitProcess();
                lblCargando.setVisible(true);
                controllerPerson2.findAllPersons();
                lblCargando.setVisible(false);
            }
        };
        Thread hilo = new Thread(ru);
        hilo.start();
    }//GEN-LAST:event_btnAgregarActionPerformed

    /**
     * Eliminamos un registro de la base de datos
     *
     * @return void
     */
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        IPersonaController controllerPerson3 = this.intancePersonaControlle();
        Runnable ru = () -> {
            if (this.tblPersonas.getSelectedRow() >= 0) {
                lblCargando.setVisible(true);
                controllerPerson3.deleteOnePerson();
                lblCargando.setVisible(false);
                this.whaitProcess();
                lblCargando.setVisible(true);
                controllerPerson3.findAllPersons();
                lblCargando.setVisible(false);
            }
        };
        Thread hilo = new Thread(ru);
        hilo.start();
    }//GEN-LAST:event_btnEliminarActionPerformed
    
