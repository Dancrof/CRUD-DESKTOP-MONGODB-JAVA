    /**
     * Validamos si los campos estan vacios o tienen valores incorrectos
     *
     * @return boolean
     */
    private boolean valiadarCampos() {
        if (txtNombresCompletos.getText().isEmpty() || txtNombresCompletos.getText().matches("[a-zA-Z ]")) {
            JOptionPane.showMessageDialog(null, "Campo Nombres Completos esta vacio");
            return false;
        }
        if (txtApellidosCompletos.getText().isEmpty() || txtApellidosCompletos.getText().matches("[a-zA-Z ]")) {
            JOptionPane.showMessageDialog(null, "Campo Apellidos Completos esta vacio o no contiene letras");
            return false;
        }
        if (txtTelefono.getText().isEmpty() || !txtTelefono.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "Campo telefono esta vacio o no contiene numeros");
            return false;
        }
        if (txtDireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Direccion esta vacio");
            return false;
        }
        return true;
    }

    /**
     * Obtenemos una instancia de la IPersonaController
     *
     * @return IPersonaController
     */
    private IPersonaController intancePersonaControlle() {
        return new PersonaController(this.txtNombresCompletos, this.txtApellidosCompletos,
                this.txtTelefono,
                this.txtDireccion,
                this.tblPersonas,
                this.brrCarga
        );
    }

    /**
     * Espera 900 milisegundos para avanza a la sigiente linea
     *
     * @return void
     */
    private void whaitProcess() {
        try {
            Thread.sleep(900);
        } catch (InterruptedException ex) {
            Logger.getLogger(AppView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
